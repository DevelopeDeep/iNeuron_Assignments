# Q. 6. How do you tell the difference between the equal to
# and assignment operators?Describe a condition and when you would use one ?

# Ans: == is the equal to operator that compares two values and evaluates to a Boolean,
# while = is that assignment operator that stores a value in a variable.


def main():
    a = 3               # Assigning operator that stores 3 value in a variable a.
    if a == 3:          # comparing values of a variable value and 3.
        print(a == 3)

if __name__=="__main__":
    main()
