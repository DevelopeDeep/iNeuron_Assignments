# Q. 5 What are the six comparison operators?

# ==, !=, <, >, <=, =>

def main():
    a = 100
    b = 200
    print("Ans : ", (a == 100))
    print("Ans : ", (a != b))
    print("Ans : ", (a < b))
    print("Ans : ", (b > a))
    print("Ans : ", (a <= b))
    print("Ans : ", (b >= a))

if __name__=="__main__":
    main()
